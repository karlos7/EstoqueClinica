    private boolean verificaQuantidadeEstoque(int qtdSaida, int qtdAtual) {
        if (estoque.getQtdEstoque() >= (qtdSaida + qtdAtual)) {
            return true;
        }
        JOptionPane.showMessageDialog(this, "Não foi possível realizar essa saída. A Quantidade não está disponível!\n"
                + "Nome produto: " + estoque.getProduto().getNomeProdutoConsumo()
                + "\nQuantidade em estoque: " + (estoque.getQtdEstoque() - qtdAtual));
        return false;
    }

    private int quantidadeSaida(int index) {
        int qtdAtual;
        if (index == -1) {
            if (estoque.getQtdSaida() == 0) {
                qtdAtual = estoque.getQtdSaida();
            } else {
                qtdAtual = 0;
            }
        } else if (estoque.getQtdSaida() == 0) {
            qtdAtual = saidaEstoque.getEstoqueConsumo().get(index).getQtdSaida();
        } else if (estoque.getQtdSaida() != saidaEstoque.getEstoqueConsumo().get(index).getQtdSaida()) {
            qtdAtual = saidaEstoque.getEstoqueConsumo().get(index).getQtdSaida() - estoque.getQtdSaida();

        } else {
            qtdAtual = 0;
        }
        int quant = Integer.parseInt(JOptionPane.showInputDialog(rootPane, "Quantidade de itens para saída.\n\n"
                + "Nome produto: " + estoque.getProduto().getNomeProdutoConsumo() + "\n"
                + "Quantidade em estoque: " + (estoque.getQtdEstoque() - qtdAtual)));

        return quant;
    }

    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if (destino.getIdDestino() != 0) {
            List<EstoqueConsumo> lista;
            lista = (estoqueDAO.estoquesMaioresQueZero("qtdEstoque", 0));
            lista.sort((a, b) -> a.compareTo(b));
            EstoqueConsumoTableModel itm = new EstoqueConsumoTableModel(lista);
            Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Estoque");
            if (objetoRetorno != null) {
                estoque = estoqueDAO.consultarObjetoId("idEstoque", objetoRetorno);
                int index = retornaIndice(estoque);
                qtd = quantidadeSaida(index);
                if (qtd != 0) {
                    if (index != -1) {
                        if (verificaQuantidadeEstoque(qtd, (saidaEstoque.getEstoqueConsumo().get(index).getQtdSaida() - estoque.getQtdSaida()))) {
                            saidaEstoque.getEstoqueConsumo().get(index).setQtdSaida(saidaEstoque.getEstoqueConsumo().get(index).getQtdSaida() + qtd);
                            saidaEstoque.getEstoqueConsumo().get(index).setQtdEstoque(saidaEstoque.getEstoqueConsumo().get(index).getQtdEstoque() - qtd);
                            quantidadeSaidas.set(index, quantidadeSaidas.get(index) + qtd);
                            saidaEstoque.getEstoqueConsumo().get(index).setQtdSaidaAtual(saidaEstoque.getEstoqueConsumo().get(index).getQtdSaidaAtual() + qtd);
                        }
                    } else if (verificaQuantidadeEstoque(qtd, 0)) {
                        estoque.setQtdEstoque(estoque.getQtdEstoque() - qtd);
                        estoque.setQtdSaida(estoque.getQtdSaida() + qtd);
                        saidaEstoque.getEstoqueConsumo().add(estoque);
                        quantidadeSaidas.add(qtd);
                        estoque.setQtdSaidaAtual(estoque.getQtdSaidaAtual() + qtd);
                    }
                    tabelaSaida();
                } else {
                    JOptionPane.showMessageDialog(this, "Você digitou o valor 0. Não foi possivel fazer a retirada.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um destino!");
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

    private int retornaIndice(EstoqueConsumo estoqueConsumo) {
        for (int i = 0; i < saidaEstoque.getEstoqueConsumo().size(); i++) {
            if (saidaEstoque.getEstoqueConsumo().get(i).getIdEstoque() == estoqueConsumo.getIdEstoque()) {
                return i;
            }
        }
        return -1;
    }


