    private void limparCampos() {
        Util.limparCamposGenerico(this);
        estoque = new EstoqueConsumo();
        quantidadeSaidas = new ArrayList<>();
        produto = new ProdutoConsumo();
        saidaEstoque = new SaidaConsumo();
        destino = new Destino();
        tabelaSaida();
    }

    private void btSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvar1ActionPerformed
        if (!txtDestino.getText().equals("")) {
            saidaEstoque.setDataSaida(new Date());
            saidaEstoque.setDestino(destino);
            saidaEstoque.setQuantidadeSaidaDestino(quantidadeSaidas);
            
            saidaDAO.adicionar(saidaEstoque);
            JOptionPane.showMessageDialog(null, "Saída realizada com sucesso!");
            Relatorios.relatorioSaidaEstoqueConsumo(saidaEstoque.getEstoqueConsumo(), usuario, destino);

        } else {
            JOptionPane.showMessageDialog(this, "Não é possivel fazer a retirada. Selecione um destino.");
        }
        limparCampos();
    }//GEN-LAST:event_btSalvar1ActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int row = tbSaida.getSelectedRow();
        if (row > -1) {
            saidaEstoque.getEstoqueConsumo().remove(row);
            quantidadeSaidas.remove(row);
            tabelaSaida();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o Ítem!",
                    "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

