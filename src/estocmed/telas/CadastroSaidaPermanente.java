    private void btSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvar1ActionPerformed
        if (!(txtSetor.getText().equals("") && txtProduto.getText().equals(""))) {
            saidaEstoque.setDataSaida(new Date());
            saidaEstoque.setSetor(setor);
            saidaDAO.adicionar(saidaEstoque);
            JOptionPane.showMessageDialog(null, "Sa√≠da realizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "N√£o √© possivel fazer a retirada. Selecione Setor e Produto.");
        }
        limparCampos();
    }//GEN-LAST:event_btSalvar1ActionPerformed

    private void limparCampos() {
        Util.limparCamposGenerico(this);
        estoque = new EstoquePermanente();
        produtoPermanente = new ProdutoPermanente();
        saidaEstoque = new SaidaPermanente();
        setor = new Setor();
        tabelaSaida();
    }

