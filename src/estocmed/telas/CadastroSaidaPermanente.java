    private boolean verificarEstoque(EstoquePermanente estoque) {
        for (EstoquePermanente estoquePermanente : saidaEstoque.getEstoquePermanente()) {
            if (estoque.getIdEstoquePermanente() == estoquePermanente.getIdEstoquePermanente()) {
                return true;
            }
        }
        return false;
    }

    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if (setor.getIdSetor() != 0 && produtoPermanente.getIdProdutoPermanente() != 0) {
            List<EstoquePermanente> lista;
            lista = estoqueDAO.checkExistseq("produtoPermanente.idProdutoPermanente", produtoPermanente.getIdProdutoPermanente());
            EstoquePermanenteTableModel itm = new EstoquePermanenteTableModel(lista);
            Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Estoque");
            if (objetoRetorno != null) {
                estoque = estoqueDAO.consultarObjetoId("idEstoquePermanente", objetoRetorno);
                if (!estoque.isStatus()) {
                    if (!verificarEstoque(estoque)) {
                        estoque.setStatus(true);
                        saidaEstoque.getEstoquePermanente().add(estoque);
                        tabelaSaida();
                    } else {
                        JOptionPane.showMessageDialog(this, "Produto j√° est√° na lista!");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Produto j√° est√° emprestado!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o produto e o setor!");
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

