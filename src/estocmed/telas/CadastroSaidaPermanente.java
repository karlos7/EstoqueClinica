    private void btnProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdutoActionPerformed
        List<ProdutoPermanente> lista;
        lista = (produtoPermanenteDAO.listar());
        ProdutoPermanenteTableModel itm = new ProdutoPermanenteTableModel(lista);
        Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Produto Permanente");
        if (objetoRetorno != null) {
            produtoPermanente = produtoPermanenteDAO.consultarObjetoId("idProdutoPermanente", objetoRetorno);
            txtProduto.setText(produtoPermanente.getNomeProdutoPermanente());
            txtProduto.setEnabled(false);
            listaEstoqueCadastro = estoqueDAO.checkExistseq("produtoPermanente.idProdutoPermanente", produtoPermanente.getIdProdutoPermanente());
        }
    }//GEN-LAST:event_btnProdutoActionPerformed

