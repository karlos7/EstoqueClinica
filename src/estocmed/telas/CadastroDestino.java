    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        List<Destino> lista;
        lista = destinoDAO.listar();
        DestinoTableModel itm = new DestinoTableModel(lista);
        Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Destino");
        if (objetoRetorno != null) {
            destino = destinoDAO.consultarObjetoId("idDestino", objetoRetorno);
            txtNomeLocal.setText(destino.getNomeDestino());
            txtNomeCidade.setText(destino.getMunicipioDestino());
            btExcluir.setEnabled(true);

        }

    }//GEN-LAST:event_btPesquisarActionPerformed

