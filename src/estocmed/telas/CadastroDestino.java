    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        if (txtNomeLocal.getText().equals("") || txtNomeCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Prencha todos os campos !!");
        } else {
            destino.setMunicipioDestino(txtNomeCidade.getText());
            destino.setNomeDestino(txtNomeLocal.getText());
            destinoDAO.salvar(destino);
            Util.limparCamposGenerico(this);
            
        }
        destino = new Destino();
    }//GEN-LAST:event_btSalvarActionPerformed

