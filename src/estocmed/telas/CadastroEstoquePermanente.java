    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        
        if (produto.getIdProdutoPermanente() != 0) {
            //listaEstoqueCadastro = new ArrayList<>();
            listaEstoqueCadastro = estoqueDAO.checkExistseq("produtoPermanente.idProdutoPermanente", produto.getIdProdutoPermanente());
            if (listaEstoqueCadastro != null) {
                atualizarTabelaEstoque();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o produto!");
        }

    }//GEN-LAST:event_btPesquisarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (tbEstoque.getSelectedRow() > -1) {
            if (listaEstoqueCadastro.get(tbEstoque.getSelectedRow()).getIdEstoquePermanente() != 0) {
                estoqueDAO.excluir(listaEstoqueCadastro.get(tbEstoque.getSelectedRow()));
            }
            listaEstoqueCadastro.remove(tbEstoque.getSelectedRow());
            atualizarTabelaEstoque();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um produto!",
                    "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btExcluirActionPerformed


    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String estoquesSalvos = "";
        for (EstoquePermanente estoquePermanente : listaEstoqueCadastro) {
            if (estoque.getIdEstoquePermanente() == 0) {
                if (estoqueDAO.salvar(estoquePermanente)) {
                    estoquesSalvos = "PRODUTO: " + estoquePermanente.getProdutoPermanente().getNomeProdutoPermanente()
                            + "   TOMBO: " + estoquePermanente.getTomboProdutoPermanente() + "\n" + estoquesSalvos;
                    produto = new ProdutoPermanente();
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Lista de estoques do produto: " + txtProduto.getText() + "\n\n" + estoquesSalvos);
        btLimparActionPerformed(null);
    }//GEN-LAST:event_btSalvarActionPerformed

