    public void addProdutos() {

        if (produto != null) {
            InformarDataETombo a = new InformarDataETombo(true);
            a.setVisible(true);
            estoque = new EstoquePermanente();
            estoque.setProdutoPermanente(produto);
            estoque.setDataEntradaPermanente(new Date());
            estoque.setVencimentoPermanente(a.retornarData());
            estoque.setTomboProdutoPermanente(a.retornarTombo());
            listaEstoqueCadastro.add(estoque);
            atualizarTabelaEstoque();
        } else {
            System.out.println("Selecione um produto!");
        }
    }

