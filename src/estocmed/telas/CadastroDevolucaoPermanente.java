    private void btnProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdutoActionPerformed
        List<ProdutoPermanente> lista;
        lista = (produtoPermanenteDAO.listar());
        ProdutoPermanenteTableModel itm = new ProdutoPermanenteTableModel(lista);
        Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Produto Permanente");
        if (objetoRetorno != null) {
            produtoPermanente = produtoPermanenteDAO.consultarObjetoId("idProdutoPermanente", objetoRetorno);
            txtProduto.setText(produtoPermanente.getNomeProdutoPermanente());
            txtProduto.setEnabled(false);
            listaEstoque = estoqueDAO.checkExistseq("produtoPermanente.idProdutoPermanente", produtoPermanente.getIdProdutoPermanente());
            listaEstoque.sort((a, b) -> b.compareTo(a));
            tabelaSaida();
        }
    }//GEN-LAST:event_btnProdutoActionPerformed

    private int capturarLinhaSelecionada() {
        int row = tbSaida.getSelectedRow();
        if (row > -1) {
            return row;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o √çtem!",
                    "ERRO", JOptionPane.ERROR_MESSAGE);
            return -1;
        }
    }

    private void btDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDevolverActionPerformed
        if (capturarLinhaSelecionada() != -1) {
            listaEstoque.get(capturarLinhaSelecionada()).setStatus(false);
            tabelaSaida();
        }
    }//GEN-LAST:event_btDevolverActionPerformed

