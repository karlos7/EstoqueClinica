    private void btSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvar1ActionPerformed
        if (produtoPermanente.getIdProdutoPermanente() != 0) {
            for (EstoquePermanente estoquePermanente : listaEstoque) {
                estoqueDAO.atualizar(estoquePermanente);
            }
            JOptionPane.showMessageDialog(null, "Devolu√ß√£o realizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "N√£o √© poss√≠vel fazer a devolu√ß√£o. Selecione o Produto.");
        }
        limparCampos();
    }//GEN-LAST:event_btSalvar1ActionPerformed

    private void limparCampos() {
        Util.limparCamposGenerico(this);
        estoque = new EstoquePermanente();
        produtoPermanente = new ProdutoPermanente();
        listaEstoque = new ArrayList<>();
        setor = new Setor();
        tabelaSaida();
    }

