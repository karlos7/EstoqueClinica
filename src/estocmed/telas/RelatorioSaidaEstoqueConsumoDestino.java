    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        List<SaidaConsumo> saidas = new ArrayList<>();

        //Instrução para filtrar os dados
        if (txtDestino.getText().equals("") && (txtDataFinal.getText().equals("  /  /    ") && txtDataInicial.getText().equals("  /  /    "))) {
            JOptionPane.showMessageDialog(this, "Campos de pesquisa vazios!");
        } else {
            if (!txtDestino.getText().equals("") && (txtDataFinal.getText().equals("  /  /    ") && txtDataInicial.getText().equals("  /  /    "))) {
                saidas = saidaConsumoDAO.checkExistsRelatorio("destino.idDestino", destino.getIdDestino());

            } else if ((!(txtDataFinal.getText().equals("  /  /    ") || txtDataInicial.getText().equals("  /  /    "))) && txtDestino.getText().equals("")) {
                saidas = saidaConsumoDAO.listaEntreDatas("dataSaida", formataData(txtDataInicial.getText()), formataData(txtDataFinal.getText()));

            } else if (!txtDestino.getText().equals("") && (!(txtDataFinal.getText().equals("  /  /    ") && txtDataInicial.getText().equals("  /  /    ")))) {
                saidas = saidaConsumoDAO.listaEntreDatasECampo("dataSaida",
                        formataData(txtDataInicial.getText()), formataData(txtDataFinal.getText()), "destino.idDestino", destino.getIdDestino());
            }

            if (!saidas.isEmpty()) {
                Relatorios.gerarRelatorioTabelaSaida(saidas);
                destino = new Destino();
                Util.limparCamposGenerico(this);
            } else {
                JOptionPane.showMessageDialog(this, "A pesquisa não encontrou nenhum arquivo!");
                destino = new Destino();
                Util.limparCamposGenerico(this);
            }

        }


    }//GEN-LAST:event_btPesquisarActionPerformed

    private Date formataData(String data) {
        if (data == null || data.equals("")) {
            return null;
        }
        Date d = null;
        try {
            DateFormat formatar = new SimpleDateFormat("dd/MM/yyyy");
            d = formatar.parse(data);
        } catch (ParseException e) {

        }
        return d;
    }

