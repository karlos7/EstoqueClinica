    private void btnSetorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetorActionPerformed
        List<Destino> lista;
        lista = (destinoDAO.listar());
        DestinoTableModel itm = new DestinoTableModel(lista);
        Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Destino");
        if (objetoRetorno != null) {
            destino = destinoDAO.consultarObjetoId("idDestino", objetoRetorno);
            txtDestino.setText(destino.getNomeDestino());
            txtDestino.setEnabled(false);
        }
    }//GEN-LAST:event_btnSetorActionPerformed

