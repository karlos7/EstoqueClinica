    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        List<EstoqueConsumo> lista;
        lista = (estoqueDAO.listar());
        EstoqueConsumoTableModel itm = new EstoqueConsumoTableModel(lista);
        Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Produto");
        if (objetoRetorno != null) {
            estoque = estoqueDAO.consultarObjetoId("idEstoque", objetoRetorno);
            String quantidade = estoque.getQtdEstoque().toString();
            curso = cursoDAO.consultarObjetoId("idCurso", estoque.getCurso().getIdCurso());
            produto = produtoDAO.consultarObjetoId("idProduto", estoque.getProduto().getIdProduto());
            txtQuantidade.setText(quantidade);
            txtVencimento.setText(formatarData.format(estoque.getVencimentoProdConsumo()));
            txtProduto.setText(produto.getNomeProdutoConsumo());
            txtCurso.setText(curso.getNomeCurso());
            if (estoque.getTipoEntrada().equalsIgnoreCase(jrEstagio.getText())) {
                jrEstagio.setSelected(true);
            }
            if (estoque.getTipoEntrada().equalsIgnoreCase(jrLaboratorio.getText())) {
                jrLaboratorio.setSelected(true);
            }

            btExcluir.setEnabled(true);
        }


    }//GEN-LAST:event_btPesquisarActionPerformed

    private String converterDataString(Date date) {
        SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy");
        return f.format(date);
    }
    
    private Date formataData(String data) {
        if (data == null || data.equals("")) {
            return null;
        }
        Date d = null;
        try {
            DateFormat formatar = new SimpleDateFormat("dd/MM/yyyy");
            d = formatar.parse(data);
        } catch (ParseException e) {

        }
        return d;
    }
    
