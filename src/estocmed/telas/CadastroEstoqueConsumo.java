    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        if (txtProduto.getText().equals("") || txtCurso.getText().equals("") || txtQuantidade.getText().equals("") || txtVencimento.getText().equals("") || !(jrEstagio.isSelected() || jrLaboratorio.isSelected())) {
            JOptionPane.showMessageDialog(this, "Prencha todos os campos !!");
        } else {
            estoque.setQtdEstoque(Integer.parseInt(txtQuantidade.getText()));
            estoque.setVencimentoProdConsumo(formataData(txtVencimento.getText()));
            if (jrEstagio.isSelected()) {
                estoque.setTipoEntrada(jrEstagio.getText());
            } else if (jrLaboratorio.isSelected()) {
                estoque.setTipoEntrada(jrLaboratorio.getText());
            }
            estoque.setProduto(produto);
            
            estoque.setCurso(curso);
            estoque.setDataEntrada(new Date());
            estoque.setQtdSaida(0);
            estoqueDAO.salvar(estoque);
            grupoDestino.clearSelection();
            btLimparActionPerformed(null);
            estoque = new EstoqueConsumo();

        }
    }//GEN-LAST:event_btSalvarActionPerformed

